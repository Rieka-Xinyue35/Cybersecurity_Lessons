number1 = int(input("Enter your first number: "))
number2 = int(input("Enter your second number: "))

add = number1 + number2
subtract = number1 - number2
multiply = number1 * number2
divide = number1 / number2
floor_divide = number1 // number2
modulus = number1 % number2
exponent = number1 ** number2

print(f"{number1} + {number2} = {add}")
print(f"{number1} - {number2} = {subtract}")
print(f"{number1} * {number2} = {multiply}")
print(f"{number1} / {number2} = {divide}")
print(f"{number1} // {number2} = {floor_divide}")
print(f"{number1} % {number2} = {modulus}")
print(f"{number1} ** {number2} = {exponent}")